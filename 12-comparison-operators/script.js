// greater
console.log(5 > 3);

// less
console.log(5 < 3);

// greater or equal
console.log(5 >= 3);

// less or equal
console.log(5 <= 3);

// equal
console.log(5 == 3);
console.log(5 == "5");

// strict equal
console.log(5 === 3);
console.log(5 === "5");

// not equal
console.log(5 != 3);
console.log(5 != "5");

// strict not equal
console.log(5 !== 3);
console.log(5 !== "5");

// all comparison operators return a boolean value
