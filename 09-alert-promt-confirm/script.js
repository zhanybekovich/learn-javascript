// alert for showing messages
alert("hello, world!");

// prompt for getting user input
let username = prompt("What is your name?");
alert(`Hello, ${username}!`);

// prompt with default value
let username2 = prompt("What is your name?", "John");
alert(`Hello, ${username2}!`);

// confirm for getting user confirmation
let isAgree = confirm("Do you agree?");
alert(isAgree); // true or false
